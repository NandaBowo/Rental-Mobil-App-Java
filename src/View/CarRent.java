    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tenantName = tenantField.getText();
        String rentDate = rentDateField.getText();
        String rentDuration = durationField.getText();
        String carCode = carCodeField.getText();
        
        RentAbstract rent = new RentExtends();
        
        if (carCode.equals("") || tenantName.equals("") || rentDate.equals("") || rentDuration.equals("")) {
            JOptionPane.showMessageDialog(this, "Tolong isi semua kolom kosong!");
        } else {
            boolean rentAddStatus = rent.add(carCode, tenantName, rentDate, rentDuration);
            
            if (rentAddStatus == true) {
                JOptionPane.showMessageDialog(this, "Data successfully added!");
            }

            carCodeField.setText("");
            tenantField.setText("");
            rentDateField.setText("");
            durationField.setText("");

            showTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

