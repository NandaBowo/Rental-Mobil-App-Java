private void showTable() {
        RentAbstract rent = new RentExtends();
        CarModel[] carList = rent.getAllWhere("status", "Tersedia");
        
        DefaultTableModel tbl = (DefaultTableModel) carMasterTable.getModel();
        tbl.setRowCount(0);
        
        for (var i : carList) {
            tbl.addRow(new Object[] {
                i.getCarCode(),
                i.getCarName(),
                i.getBrand(),
                i.getPassenger(),
                i.getPlate(),
            });
        }
    }

