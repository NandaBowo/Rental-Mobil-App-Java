    private void showTable() {
        RentAbstract rent = new RentExtends();
        RentModel[] rentList = rent.getAll();
        
        DefaultTableModel tbl = (DefaultTableModel) rentListTable.getModel();
        tbl.setRowCount(0);
        
        for (var i : rentList) {
            tbl.addRow(new Object[] {
                i.getCarCode(),
                i.getTenantName(),
                i.getRentDate(),
                i.getRentDuration()
            });
        }
    }
