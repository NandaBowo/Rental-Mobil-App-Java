    private void hapusDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusDataBtnActionPerformed
        // TODO add your handling code here:
        RentAbstract rent = new RentExtends();
        
        String carCode = carCodeField.getText();
        
        int deleteConfirmation = JOptionPane.showConfirmDialog(this, "Ingin menghapus data ini?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        
        if (deleteConfirmation == 0) {
            boolean deleteExcecuted = rent.delete(carCode);
            
            if (deleteExcecuted == true) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus!");
            }
            
            showTable();
            
            carCodeField.setText("");
        }
    }//GEN-LAST:event_hapusDataBtnActionPerformed

