    private void showTable() {
        CarInterface car = new CarImplements();
        CarModel[] carList = car.getAll();
        
        DefaultTableModel tbl = (DefaultTableModel) carMasterTable.getModel();
        tbl.setRowCount(0);
        
        for (var i : carList) {
            tbl.addRow(new Object[] {
                i.getCarCode(),
                i.getCarName(),
                i.getBrand(),
                i.getPassenger(),
                i.getPlate(),
                i.getStatus()
            });
        }
    }
