    private void tambahDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahDataBtnActionPerformed
        // TODO add your handling code here:
        String carCode = carCodeField.getText();
        String carName = carNameField.getText();
        String carBrand = brandField.getText();
        String carCapacity = capacityField.getText();
        String carPlate = plateNumField.getText();
        String carStatus = carStatusField.getSelectedItem().toString();
        
        CarInterface car = new CarImplements();
        
        if (carCode.equals("") || carName.equals("") || carBrand.equals("") || carCapacity.equals("") || carPlate.equals("") || carStatus.equals("")) {
            JOptionPane.showMessageDialog(this, "Tolong isi semua kolom kosong dibawah!");
        } else {
            try {
                boolean addStatus = car.add(carCode, carName, carBrand, carCapacity, carPlate, carStatus);
                
                if (addStatus == true) {
                    JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Masukkan angka ke dalam kolom kapasitas penumpang!");
            }

            carCodeField.setText("");
            carNameField.setText("");
            brandField.setText("");
            capacityField.setText("");
            plateNumField.setText("");
            carStatusField.setSelectedIndex(0);

            refreshDataBtnActionPerformed(evt);
        }
    }//GEN-LAST:event_tambahDataBtnActionPerformed

    private void carMasterTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_carMasterTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tbl = (DefaultTableModel) carMasterTable.getModel();
        String selectCarCode = tbl.getValueAt(carMasterTable.getSelectedRow(), 0).toString();
        String selectCarName = tbl.getValueAt(carMasterTable.getSelectedRow(), 1).toString();
        String selectCarBrand = tbl.getValueAt(carMasterTable.getSelectedRow(), 2).toString();
        String selectCarCapacity = tbl.getValueAt(carMasterTable.getSelectedRow(), 3).toString();
        String selectCarPlate = tbl.getValueAt(carMasterTable.getSelectedRow(), 4).toString();
        
        carCodeField.setText(selectCarCode);
        carNameField.setText(selectCarName);
        brandField.setText(selectCarBrand);
        capacityField.setText(selectCarCapacity);
        plateNumField.setText(selectCarPlate);
        carStatusField.setSelectedIndex(0);
    }//GEN-LAST:event_carMasterTableMouseClicked

    private void ubahDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahDataBtnActionPerformed
        // TODO add your handling code here:
        if (carMasterTable.getSelectedRowCount() == 1) {
            String carCode = carCodeField.getText();
            String carName = carNameField.getText();
            String carBrand = brandField.getText();
            String carCapacity = capacityField.getText();
            String carPlate = plateNumField.getText();
            String carStatus = carStatusField.getSelectedItem().toString();
            
            CarInterface car = new CarImplements();
            
            try {
                boolean editStatus = car.edit(carCode, carName, carBrand, carCapacity, carPlate, carStatus);
                
                if (editStatus == true) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Masukkan angka ke dalam kolom kapasitas penumpang!");
            }
            
            carCodeField.setText("");
            carNameField.setText("");
            brandField.setText("");
            capacityField.setText("");
            plateNumField.setText("");
            carStatusField.setSelectedIndex(0);
            
            refreshDataBtnActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(this, "Tolong pilih satu data!");
        }
    }//GEN-LAST:event_ubahDataBtnActionPerformed

    private void hapusDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusDataBtnActionPerformed
        // TODO add your handling code here:
        CarInterface car = new CarImplements();
        
        String carCode = carCodeField.getText();
        
        int deleteConfirmation = JOptionPane.showConfirmDialog(this, "Ingin menghapus data ini?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        
        if (deleteConfirmation == 0) {
            boolean deleteExcecuted = car.delete(carCode);
            
            if (deleteExcecuted == true) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus!");
            }
            
            refreshDataBtnActionPerformed(evt);
        }
        
        carCodeField.setText("");
        carNameField.setText("");
        brandField.setText("");
        capacityField.setText("");
        plateNumField.setText("");
        carStatusField.setSelectedIndex(0);
    }//GEN-LAST:event_hapusDataBtnActionPerformed

